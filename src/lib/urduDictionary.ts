export function translateToUrdu(text: string): string {
  return text.split(" ").map(word => {
    // Clean punctuation (e.g., "." or ",")
    const cleanWord = word.toLowerCase().replace(/[^a-z]/gi, "")
    return urduDictionary[cleanWord] || word
  }).join(" ")
}
